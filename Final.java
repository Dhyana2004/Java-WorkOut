public class Final {
    // final keyword in java
    public static void main(String[] args) {
        double a=3;
        System.out.println(a);
        a=5;
        System.out.println(a);

        final double pi=3.14;
        System.out.println(pi);

      /* 
      final assigned variables cannot be changed
        pi=3.14566;
        System.out.println(pi);
        */
    }
}
